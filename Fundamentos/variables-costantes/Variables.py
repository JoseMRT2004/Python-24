
# Variables en Python Una variable es un contenedor para datos.

# Sección: Tipos de Datos
# Ejemplo de variable (int)
mi_variable = 10
print(mi_variable)

# Cambiar valor de la variable
mi_variable = 20
print(mi_variable)

# Ejemplo de constante (float)
MI_CONSTANTE = 3.14
print(MI_CONSTANTE)

# Cambiar valor de la constante (no recomendado)
MI_CONSTANTE = 2.71
print(MI_CONSTANTE)

# Nota: No es buena práctica cambiar constantes.

# Ejemplo de variable (str)
mi_cadena = "Hola, Mundo!"
print(mi_cadena)

# Ejemplo de variable (bool)
mi_booleano = True
print(mi_booleano)

# Ejemplo de variable (list)
mi_lista = [1, 'Hola', 10,2, 4, 5]
print(mi_lista[-3])

# Ejemplo de variable (dict)
mi_diccionario = {"nombre": "Maria", "edad": 24}
print(mi_diccionario)




