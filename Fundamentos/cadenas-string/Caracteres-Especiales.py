# Lista de caracteres especiales en Python

print('Hola \nmundo')  # \n genera un salto de línea en el texto, separando "Hola" y "mundo"
print('\t\tPython es divertido')  # \t agrega una tabulación (espacios) antes del texto
print('Juan\' "Perez"')  # \' escapa la comilla simple para que no cierre la cadena
print("Juan \"Perez")  # \" escapa la comilla doble dentro de una cadena de comillas dobles
print('Caracter \\ Diagonal invertida')  # \\ representa una barra invertida dentro de una cadena
