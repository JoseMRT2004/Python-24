# Reemplazo de subcadenas en una cadena original

# Definimos la cadena original
cadena = 'Hola, mundo!'
print(f'Cadena original: {cadena}')  # Imprimimos la cadena original

# Reemplazamos 'mundo' por 'a todos'
nueva_cadena = cadena.replace('mundo', 'a todos')
print(f'Nueva cadena reemplazada: {nueva_cadena}')  # Imprimimos la nueva cadena

# Ahora, reemplazamos 'Hola' por 'Adios'
nueva_cadena = cadena.replace('Hola', 'Adios')
print(f'Nueva cadena reemplazada: {nueva_cadena}')  # Imprimimos la nueva cadena despu√©s del segundo reemplazo
